int	ft_sqrt(int nb)
{
	int i;

	i = 0;
	if (nb <= 0)
	{
		return (0);
	}
	else
	{
		while (((i * i) < nb) && (i <= 46340))
		{
			i++;
		}
		if ((i * i) == nb)
		{
			return (i);
		}
		else
		{
			return (0);
		}
	}
}
